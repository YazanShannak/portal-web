<template>
  <div class="preview my-8 flex flex-wrap md:flex-no-wrap">

    <appImage v-if="publication.thumbnail" class="thumbnail" :image="publication.thumbnail" size="medium" />
    <img v-else class="thumbnail" :src="placeholderImage" />

    <div ref="content" class="flex-grow">

      <h3 class="uppercase py-2 md:pt-0 text-sm">
        <span v-for="(topic, index) in publication.topics" :key="topic.id">
          {{ topic['title_' + $i18n.locale] }}{{ index != Object.keys(publication.topics).length - 1 ? ', ' : ''}}
        </span>
      </h3>

      <nuxt-link :to="publicationLink">
        <h2 class="mb-4 text-3xl">{{ publication['title_' + $i18n.locale] }}</h2>
      </nuxt-link>

      <div class="publication-info flex flex-wrap md:flex-no-wrap">
        <span class="ltr:mr-12 rtl:ml-12">{{ $t('meta.by') }} {{ publication['author_' + $i18n.locale]}}</span>
        <span>
          <font-awesome-icon class="icon" :icon="['fas', 'clock']" />{{ $t('publication.publishDate') }}
          {{ publication.publishDate | fullDate($i18n.locale) }}</span>

      </div>

      <nuxt-link :to="publicationLink"
        class="block py-4 text-josa-blue font-bold ltr:text-sm rtl:text-base hover:opacity-75">
        {{ $t('meta.readTheReport') }}
        <font-awesome-icon class="ltr:ml-2 rtl:mr-2 align-middle" :icon="['fas', arrowIcon ]" />
      </nuxt-link>

    </div>
  </div>
</template>

<script>
  import appImage from '~/components/UI/appImage';
  export default {
    name: 'PublicationPreview',
    data() {
      return {
        placeholderImage: process.env.baseUrl + '/uploads/josabots_88f0a93786.jpeg'
      }
    },
    components: {
      appImage
    },
    props: {
      publication: {
        type: Object,
        required: true
      }
    },
    computed: {
      publicationLink() {
        const title = this.publication['title_' + this.$i18n.locale]
        const slug = this.$options.filters.stringToSlug(title)
        return this.localePath('/publications/' + this.publication.id + '/' + slug)
      },
      arrowIcon() {
        if (this.$i18n.locale == "ar") {
          return 'long-arrow-alt-left'
        } else {
          return 'long-arrow-alt-right'
        }
      }
    }
  }

</script>

